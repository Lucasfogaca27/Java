    private void cadastrarAlterar() {
        if(txtNome.getText().equals("")){
            JOptionPane.showMessageDialog(null,
                    "Digite o nome!!!", "Nome",
                    JOptionPane.ERROR_MESSAGE);
            txtNome.requestFocus();
        } else  if(txtTelefone.getText().equals("")){
            JOptionPane.showMessageDialog(null,
                    "Digite o telefone!!", "Nome",
                    JOptionPane.ERROR_MESSAGE);
            txtTelefone.requestFocus();
        } else if (btnCadastrar.getText().equals("Cadastrar")){
            ContatoDAO contatoDAO = new ContatoDAO();
            Contato contato = new Contato (txtNome.getText().toUpperCase(), 
            txtTelefone.getText().toUpperCase());
            contatoDAO.cadastrarContato(contato);
            JOptionPane.showMessageDialog(null,
                    "Contato cadastrado com sucesso!",
                    "Cadastrado", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } else if (btnCadastrar.getText().equals("Alterar")){
            Object[] opcoes = {"Sim", "NÃ£o"};
            int retorno = JOptionPane.showOptionDialog(null,
                    "Tem certeza que deseja alterar?",
                    "Alterar", JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,null,
                    opcoes,opcoes[0]);
            if (retorno == 0){
                ContatoDAO contatoDAO = new ContatoDAO();
                Contato contato = new Contato
                (txtNome.getText().toUpperCase(),
                 txtTelefone.getText().toUpperCase());
                Contato contatoAtual = new Contato
                (nomeAtual, telefoneAtual);
                 contatoDAO.atualizarContato(contato, contatoAtual);
                 JOptionPane.showMessageDialog(null,
                         "Contato alterado com sucesso!",
                         "Alterado", JOptionPane.INFORMATION_MESSAGE);
                 dispose();
            }
        }
    }    
    
